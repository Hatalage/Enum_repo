package com.demoPrograms;

public class WCDemo2 {
public static void main(String[] args) {
	byte s=2;
	Byte a=s;
    System.out.println("byte:  "+a);
    System.out.println();
    
    short s1=12;
    Short b=s1;
    System.out.println("short:  "+b);
    System.out.println();
    
    
    long s2=1234567;
    Long c=s2;
    System.out.println("long:  "+c);
    System.out.println();
    
    
    float s3=23.12f;
    Float d=s3;
    System.out.println("float:  "+d);
    System.out.println();
    
    
    double s4=1234.6789;
    Double e=s4;
    System.out.println("Double:  "+e);
    System.out.println();
    
    
    char s5='A';
    Character f=s5;
    System.out.println("Character:  "+f);
    System.out.println();
    
}
}
